package MatchModel;

public class MatchVo {

	   private int boardId;
	   private String userId;
	   
	   public MatchVo() {
	      
	   }

	   public int getBoardId() {
	      return boardId;
	   }

	   public void setBoardId(int boardId) {
	      this.boardId = boardId;
	   }

	   public String getUserId() {
	      return userId;
	   }

	   public void setUserId(String userId) {
	      this.userId = userId;
	   }
}